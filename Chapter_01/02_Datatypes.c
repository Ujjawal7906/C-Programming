#include <stdio.h>
int main(){
    int a;
    a = 5; // Integer datatype

    float b;
    b=7.6; // Float datatype

    char c;
    c = 'g'; // Character datatype

    printf("The value of a is %d \n", a);
    printf("The value of b is %f \n", b);
    printf("The value of c is %c \n", c);
    return 0;
}